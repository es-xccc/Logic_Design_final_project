module display(clk,sel,num, ar);
input clk;
input [1:0]sel;
input [3:0]num;
output reg [11:0] ar;
always@*begin
if(sel==2'b11)begin
    case(num)
        4'd0: ar=12'b011111101011;
        4'd1: ar=12'b000111101010;
        4'd2: ar=12'b010111110011;
        4'd3: ar=12'b010111111010;
        4'd4: ar=12'b001111111010;
        4'd5: ar=12'b011110101001;
        4'd6: ar=12'b001110101011;
        4'd7: ar=12'b010111101000;
        4'd8: ar=12'b011111111011;
        4'd9: ar=12'b011111111010;
    endcase
    end
    if(sel==2'b10)begin
    case(num)
        4'd0: ar=12'b111011101011;
        4'd1: ar=12'b100011101010;
        4'd2: ar=12'b110011110011;
        4'd3: ar=12'b110011111010;
        4'd4: ar=12'b101011111010;
        4'd5: ar=12'b111010101001;
        4'd6: ar=12'b101010101011;
        4'd7: ar=12'b110011101000;
        4'd8: ar=12'b111011111011;
        4'd9: ar=12'b111011111010;
    endcase
    end
    if(sel==2'b01)begin
    case(num)
        4'd0: ar=12'b111101101011;
        4'd1: ar=12'b100101101010;
        4'd2: ar=12'b110101110011;
        4'd3: ar=12'b110101111010;
        4'd4: ar=12'b101101111010;
        4'd5: ar=12'b111100101001;
        4'd6: ar=12'b101100101011;
        4'd7: ar=12'b110101101000;
        4'd8: ar=12'b111101111011;
        4'd9: ar=12'b111101111010;
    endcase;
    end
    if(sel==2'b00)begin
    case(num)
        4'd0: ar=12'b111111001011;
        4'd1: ar=12'b100111001010;
        4'd2: ar=12'b110111010011;
        4'd3: ar=12'b110111011010;
        4'd4: ar=12'b101111011010;
        4'd5: ar=12'b111110001001;
        4'd6: ar=12'b101110001011;
        4'd7: ar=12'b110111001000;
        4'd8: ar=12'b111111011011;
        4'd9: ar=12'b111111011010;
    endcase;
    end
end
endmodule